"""Module 4: RAGAS evaluation with deterministic fallback analysis.

This module owns the evaluation step of the Production RAG pipeline:
1. Load the manually curated test set from JSON.
2. Evaluate generated answers with the four required RAGAS metrics.
3. Fall back to deterministic lexical metrics when external services are unavailable.
4. Analyze the weakest questions and map them to actionable failure causes.
5. Save aggregate scores and failures into a JSON report.

The fallback path is intentionally simple and reproducible. It is not a full
replacement for RAGAS, but it keeps tests, CI, and classroom demos usable when
OpenAI credentials, network access, or RAGAS runtime dependencies are missing.
"""

import json
import math
import os
import re
import sys
from dataclasses import dataclass
from statistics import mean
from typing import Any, TypeAlias

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from config import OPENAI_API_KEY, TEST_SET_PATH

# Shared aliases keep public function signatures readable.
MetricName: TypeAlias = str
JsonDict: TypeAlias = dict[str, Any]
EvalSummary: TypeAlias = dict[str, float | list["EvalResult"]]

# Rubric-required RAGAS metrics, reused for aggregation and diagnostics.
METRIC_NAMES: tuple[MetricName, ...] = (
    "faithfulness",
    "answer_relevancy",
    "context_precision",
    "context_recall",
)

# Thresholds encode the diagnostic tree from the assignment rubric.
DIAGNOSTIC_RULES: dict[MetricName, tuple[float, str, str]] = {
    "faithfulness": (0.85, "LLM hallucinating", "Tighten prompt and lower temperature"),
    "context_recall": (0.75, "Missing relevant chunks", "Improve chunking or add BM25/hybrid search"),
    "context_precision": (0.75, "Too many irrelevant chunks", "Add reranking or metadata filters"),
    "answer_relevancy": (0.80, "Answer does not match question", "Improve the prompt template"),
}


@dataclass
class EvalResult:
    """Evaluation result for one question-answer sample.

    Attributes:
        question: Original user question from the test set.
        answer: Answer generated by the RAG pipeline.
        contexts: Retrieved chunks used as evidence for the answer.
        ground_truth: Reference answer from the curated test set.
        faithfulness: Whether the answer is supported by retrieved contexts.
        answer_relevancy: Whether the answer addresses the input question.
        context_precision: Share of retrieved contexts that appear relevant.
        context_recall: Share of ground-truth information covered by contexts.
    """

    question: str
    answer: str
    contexts: list[str]
    ground_truth: str
    faithfulness: float
    answer_relevancy: float
    context_precision: float
    context_recall: float


def load_test_set(path: str = TEST_SET_PATH) -> list[JsonDict]:
    """Load the evaluation test set from disk.

    Args:
        path: Path to a JSON file containing a list of test question objects.

    Returns:
        A list of dictionaries, usually with keys such as question and
        ground_truth. The function intentionally returns generic JSON objects
        because the assignment test set may grow additional fields later.

    Raises:
        ValueError: If the decoded JSON root is not a list.
        json.JSONDecodeError: If the file is still invalid after removing
        common trailing commas from draft JSON files.
    """
    with open(path, encoding="utf-8") as f:
        raw = f.read()
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        cleaned = re.sub(r",\s*([}\]])", r"\1", raw)
        data = json.loads(cleaned)
    if not isinstance(data, list):
        raise ValueError("test set must be a JSON list of question objects")
    return data


def _validate_inputs(
    questions: list[str], answers: list[str], contexts: list[list[str]], ground_truths: list[str]
) -> None:
    """Validate one-to-one alignment between evaluation inputs.

    RAGAS expects all columns to have equal length. Failing fast here gives a
    clearer error than letting the dataset builder or dataframe conversion fail
    deeper in the stack.
    """
    lengths = {len(questions), len(answers), len(contexts), len(ground_truths)}
    if len(lengths) != 1:
        raise ValueError("questions, answers, contexts, and ground_truths must have equal length")


def _to_float(value: Any, default: float = 0.0) -> float:
    """Convert metric outputs to finite floats so reports remain JSON-safe.

    Some RAGAS versions may return numpy scalars, None, NaN, or inf values when
    a metric cannot be computed. The report should always contain normal JSON
    numbers, so invalid values are replaced by the provided default.
    """
    try:
        number = float(value)
    except (TypeError, ValueError):
        return default
    return default if math.isnan(number) or math.isinf(number) else number


def _as_context_list(value: Any, fallback: list[str]) -> list[str]:
    """Normalize RAGAS dataframe context cells into list[str].

    RAGAS and pandas can represent the contexts column as a list, tuple, numpy
    array, scalar string, or missing value depending on package versions. This
    helper preserves strings as one context instead of accidentally converting
    them with list("text"), which would split them into characters.
    """
    if value is None:
        return fallback
    if isinstance(value, str):
        return [value]
    if isinstance(value, list):
        return [str(item) for item in value]
    try:
        return [str(item) for item in list(value)]
    except TypeError:
        return fallback


def _tokens(text: str) -> set[str]:
    """Tokenize text into lowercase word units, including Vietnamese characters.

    The regular expression keeps Vietnamese letters in the token stream, which
    makes the fallback metrics more useful for the Vietnamese legal/financial
    documents used in this lab.
    """
    return set(re.findall(r"[\wÀ-ỹ]+", text.lower(), flags=re.UNICODE))


def _overlap_score(source: str, target: str) -> float:
    """Estimate how much of source is covered by target.

    The score is directional: overlap_score(answer, context) estimates answer
    support from contexts, while overlap_score(ground_truth, context) estimates
    recall of reference information. This directionality mirrors the intent of
    faithfulness and context recall better than a symmetric similarity score.
    """
    source_tokens = _tokens(source)
    target_tokens = _tokens(target)
    if not source_tokens or not target_tokens:
        return 0.0
    return len(source_tokens & target_tokens) / len(source_tokens)


def _heuristic_eval_result(
    question: str, answer: str, item_contexts: list[str], ground_truth: str
) -> EvalResult:
    """Build one deterministic EvalResult when RAGAS or OpenAI is unavailable.

    The heuristic approximates the four required metrics with token overlap:
    faithfulness uses answer-to-context coverage, answer_relevancy uses
    question-to-answer overlap, context_precision counts contexts that match the
    question or ground truth, and context_recall measures ground-truth coverage
    from all retrieved contexts.
    """
    joined_context = " ".join(item_contexts)
    # Relevant contexts share tokens with either the expected answer or the question.
    relevant_contexts = [
        ctx for ctx in item_contexts if _overlap_score(ground_truth, ctx) > 0 or _overlap_score(question, ctx) > 0
    ]
    context_precision = len(relevant_contexts) / len(item_contexts) if item_contexts else 0.0
    return EvalResult(
        question=question,
        answer=answer,
        contexts=item_contexts,
        ground_truth=ground_truth,
        faithfulness=_overlap_score(answer, joined_context),
        answer_relevancy=_overlap_score(question, answer),
        context_precision=context_precision,
        context_recall=_overlap_score(ground_truth, joined_context),
    )


def _heuristic_evaluate(
    questions: list[str], answers: list[str], contexts: list[list[str]], ground_truths: list[str]
) -> EvalSummary:
    """Evaluate all samples with lexical overlap metrics.

    This function exists to keep the public evaluate_ragas API stable even when
    the real RAGAS path cannot run. It produces the same output shape as RAGAS:
    aggregate metric keys plus a per_question list of EvalResult objects.
    """
    per_question = [
        _heuristic_eval_result(question, answer, item_contexts, ground_truth)
        for question, answer, item_contexts, ground_truth in zip(questions, answers, contexts, ground_truths)
    ]
    return _build_result(per_question)


def _build_result(per_question: list[EvalResult]) -> EvalSummary:
    """Combine per-question results into aggregate metric means plus raw details.

    The aggregate keys are exactly the four rubric metrics, while per_question
    keeps detailed rows for failure_analysis and optional report inspection.
    Empty input returns zero scores instead of raising statistics.StatisticsError.
    """
    aggregate = {
        metric: float(mean(getattr(item, metric) for item in per_question)) if per_question else 0.0
        for metric in METRIC_NAMES
    }
    aggregate["per_question"] = per_question
    return aggregate


def _evaluate_with_ragas(
    questions: list[str], answers: list[str], contexts: list[list[str]], ground_truths: list[str]
) -> EvalSummary:
    """Run real RAGAS evaluation and normalize its dataframe output.

    Args:
        questions: Test-set questions.
        answers: Pipeline-generated answers for each question.
        contexts: Retrieved evidence chunks for each question.
        ground_truths: Reference answers used by RAGAS metrics.

    Returns:
        The same EvalSummary shape as the fallback evaluator.

    Notes:
        Both ground_truth and reference are passed to support different RAGAS
        versions. The dataframe extraction also falls back to original inputs
        when a RAGAS version omits a display column from the returned result.
    """
    from datasets import Dataset
    from ragas import evaluate

    # RAGAS moved metrics to collections; keep fallback for older installed versions.
    try:
        from ragas.metrics.collections import (
            answer_relevancy,
            context_precision,
            context_recall,
            faithfulness,
        )
    except ImportError:
        from ragas.metrics import answer_relevancy, context_precision, context_recall, faithfulness

    dataset = Dataset.from_dict(
        {
            "question": questions,
            "answer": answers,
            "contexts": contexts,
            "ground_truth": ground_truths,
            "reference": ground_truths,
        }
    )
    result = evaluate(dataset, metrics=[faithfulness, answer_relevancy, context_precision, context_recall])
    df = result.to_pandas()
    per_question = []
    for idx, row in df.iterrows():
        # RAGAS may omit fields depending on version, so fall back to original inputs.
        per_question.append(
            EvalResult(
                question=str(row.get("question", questions[idx])),
                answer=str(row.get("answer", answers[idx])),
                contexts=_as_context_list(row.get("contexts"), contexts[idx]),
                ground_truth=str(row.get("ground_truth", row.get("reference", ground_truths[idx]))),
                faithfulness=_to_float(row.get("faithfulness")),
                answer_relevancy=_to_float(row.get("answer_relevancy")),
                context_precision=_to_float(row.get("context_precision")),
                context_recall=_to_float(row.get("context_recall")),
            )
        )
    return _build_result(per_question)


def evaluate_ragas(
    questions: list[str], answers: list[str], contexts: list[list[str]], ground_truths: list[str]
) -> EvalSummary:
    """Evaluate RAG answers with RAGAS, then fallback heuristics if needed.

    This is the public entry point used by pipeline.py and tests. It first
    validates aligned inputs, then runs RAGAS only when an OpenAI API key is
    configured. If RAGAS raises because of credentials, network, package, or
    runtime issues, the deterministic fallback still returns a complete report.
    """
    _validate_inputs(questions, answers, contexts, ground_truths)
    if OPENAI_API_KEY:
        try:
            return _evaluate_with_ragas(questions, answers, contexts, ground_truths)
        except Exception as exc:
            # Keep evaluation usable in offline CI or when RAGAS/OpenAI calls are unavailable.
            print(f"RAGAS unavailable, using heuristic evaluator: {exc}")
    return _heuristic_evaluate(questions, answers, contexts, ground_truths)


def failure_analysis(eval_results: list[EvalResult], bottom_n: int = 10) -> list[JsonDict]:
    """Analyze the weakest questions and attach actionable diagnoses.

    Args:
        eval_results: Per-question results from evaluate_ragas().
        bottom_n: Number of lowest-average-score questions to inspect.

    Returns:
        A list of dictionaries with the original question, weakest metric,
        metric score, average score, diagnosis, and suggested fix.

    The ranking uses average score across all rubric metrics so one extremely
    weak retrieval or generation dimension can bring a question into review.
    """
    # Rank by average score so the weakest samples are inspected first.
    ranked = sorted(
        eval_results,
        key=lambda item: mean(_to_float(getattr(item, metric, 0.0)) for metric in METRIC_NAMES),
    )
    failures = []
    for item in ranked[: max(bottom_n, 0)]:
        scores = {metric: _to_float(getattr(item, metric, 0.0)) for metric in METRIC_NAMES}
        worst_metric = min(scores, key=scores.get)
        diagnosis, suggested_fix = _diagnose(worst_metric, scores[worst_metric])
        failures.append(
            {
                "question": item.question,
                "worst_metric": worst_metric,
                "score": scores[worst_metric],
                "avg_score": float(mean(scores.values())),
                "diagnosis": diagnosis,
                "suggested_fix": suggested_fix,
            }
        )
    return failures


def _diagnose(metric: str, score: float) -> tuple[str, str]:
    """Map the weakest metric to a concise failure reason and remediation.

    If the metric is below its rubric threshold, return the targeted diagnosis.
    If it is above threshold, keep the question in the report but label it as a
    monitoring case rather than a critical failure.
    """
    threshold, diagnosis, suggested_fix = DIAGNOSTIC_RULES[metric]
    if score < threshold:
        return diagnosis, suggested_fix
    return "No critical failure", "Keep monitoring this query in the evaluation set"


def save_report(results: EvalSummary, failures: list[JsonDict], path: str = "ragas_report.json") -> None:
    """Save aggregate scores and diagnostic failures to a JSON report.

    Args:
        results: EvalSummary returned by evaluate_ragas().
        failures: Diagnostic rows returned by failure_analysis().
        path: Output path for the JSON report.

    The per_question objects are intentionally excluded from aggregate because
    dataclass instances are not directly JSON-serializable and the failure list
    already contains the most useful question-level debugging information.
    """
    report = {
        "aggregate": {k: v for k, v in results.items() if k != "per_question"},
        "num_questions": len(results.get("per_question", [])),
        "failures": failures,
    }
    with open(path, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)
    print(f"Report saved to {path}")


